# 假设字典dic_city中存放了每个人旅游过的城市，内容为{"张三疯":["北京","成都"],"李沫绸":["上海","广州","兰州"],"慕容福":["太原","西安","济南","上海"]}。试编写程序，根据用户输入的姓名，输出去过的城市。
# 样例1：
# 输入：
# 张三疯
# 输出：
# 张三疯去过2个城市，分别是北京、成都。
# 样例2：
# 输入：
# 张三丰
# 输出：
# 未查询到该姓名


dic_city = {"张三疯":["北京","成都"],"李沫绸":["上海","广州","兰州"],"慕容福":["太原","西安","济南","上海"]}

name = input()
if name in dic_city:
    city = "、".join(dic_city[name])  # 将去过的城市转换为题目要求的格式的字符串并命名为city
    print(f"{name}去过{len(dic_city[name])}个城市，分别是{city}。")
else:
    print("未查询到该姓名")