# 假设有三个列表：lst_who=["小马","小羊","小鹿"]，lst_where=["草地上","电影院","家里"]，lst_what=["看电影","听故事","吃晚饭"]。编写程序，由用户输入三个0~2之间的整数，将其作为索引分别访问三个列表中对应的元素并进行造句。
# 样例：
# 输入：
# 1,0,2
# 输出：
# 小羊在草地上吃晚饭


lst_who=["小马","小羊","小鹿"]
lst_where=["草地上","电影院","家里"]
lst_what=["看电影","听故事","吃晚饭"]
num = list(map(int,input().split(","))) # 获取输入的内容，并按“，”分割后将元素转换成整数存放在num的列表中
print(lst_who[num[0]],"在",lst_where[num[1]],lst_what[num[2]],sep="")